DECLARE 
SUM_1 NUMBER(10) := 0;
N NUMBER(10) := &N;
A NUMBER(10) := 0;
K NUMBER(10);
BEGIN
K := N;
LOOP
	A := MOD(N,10);
	SUM_1 := SUM_1 *10+A;
	N := TRUNC(N/10);
	EXIT WHEN N=0;
END LOOP;
IF (SUM_1 = K) THEN
DBMS_OUTPUT.PUT_LINE(K||' IS A PALINDROME NUMBER');
ELSE
DBMS_OUTPUT.PUT_LINE(K||' IS NOT A PALINDROME NUMBER');
END IF;
END;
/
